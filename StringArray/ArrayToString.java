package StringArray;

import java.util.*;

public class ArrayToString {
    public static void main(String args[]) {
        //declare new array stored in attribute x
        int[] x = {9, 5, 40, 33, 17, 2, 0};
        //print x using the Arrays class, using the toString method
        System.out.println(Arrays.toString(x));
    }
}
