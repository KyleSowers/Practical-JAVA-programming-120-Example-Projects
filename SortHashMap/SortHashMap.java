package SortHashMap;

import java.util.HashMap;

public class SortHashMap {
    public static void main(String[] args) {

        HashMap<Integer, String> hm = new HashMap<Integer, String>();
        hm.put(7, "Sunday");
        hm.put(4, "Thursday");
        hm.put(2, "Tuesday");
        hm.put(5, "Friday");
        hm.put(1, "Monday");
        hm.put(6, "Saturday");
        hm.put(3, "Wednesday");
    }
}
